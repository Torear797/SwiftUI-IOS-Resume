import SwiftUI

public extension Color {
    static let myAccentColor = Color.accentColor
    static let background = Color(.background)
    static let cardBackground = Color(.cardBackground)
    static let cardBackgroundSecondary = Color(.cardBackgroundSecondary)
    static let shadow = Color(.shadow)
    static let inactive = Color(.inactive)
    static let accentColorLight = Color(.accentColorLight)
    static let accentColorDark = Color(.accentColorDark)
    static let blackShadow = Color(.blackShadow)
    static let accentShadow = Color(.accentShadow)
    static let text = Color(.text)
    static let caption = Color(.caption)
    static let gold = Color(.gold)
    static let goldDark = Color(.goldDark)
}
